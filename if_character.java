public class if_character {
    public static void main(String[] args) {
        char c = 'E';
        if (c > 'A' && c < 'L') {
            System.out.println("Il carattere " + c + " è compreso tra A e L");
        } else {
            System.out.println("Il carattere " + c + " non è compreso tra A e L");
        }
    }
}
